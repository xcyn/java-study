package com.msb.test03;
//数据类型
//1、基本数据类型
//    1.1 数值型
//      1.1.1 整数类型(byte,short,int,long)
//      1.1.2 浮点类型(float,double)
//    1.2 字符型 (char)
//    1.3 布尔型 (boolean)
//2、引用数据类型
//      2.1 类 (class)
//      2.2 接口 (interface)
//      数组
public class TestType {
    public static void main(String[] args) {
        //        byte 1 字节 (-128, +127)
        byte a = 10;
        //        short 2 字节 (-3w多, + 3w多)
        short b = 20;
        //        int 4 字节 (正负21亿)
        int c = 30;
        //        long 8 字节 (超出int范围)
        long d = 40;
        //        浮点类型
        float e = 3.1F;
        double f = 3.1;
        //        字符类型
        char ch = '+';
        //        布尔类型
        boolean flag = false;

        //        引用数据类型
        String str = "";
        System.out.println(flag);



    }
}
